#include <SFML/Graphics.hpp>
#include "Game.h"
#include "menu.h"

using namespace sf;

int main()
{
	// Создаем окно размером 600 на 600 и частотой обновления 60 кадров в секунду
	RenderWindow window(VideoMode(600, 600), "15");
	menu(window);
	window.setFramerateLimit(60);

	Font font;
	font.loadFromFile("things/CyrilicOld.ttf");

	// Текст с обозначением клавиш
	Text text("F3 - New Game / Esc - Exit / Arrow Keys - Move Tile", font, 20);
	text.setFillColor(Color::Green);
	text.setPosition(5.f, 5.f);

	// Создаем объект игры
	game game;
	game.setPosition(50.f, 50.f);

	Event event;
	int move_counter = 0;	// Счетчик случайных ходов для перемешивания головоломки

	while (window.isOpen())
	{
		while (window.pollEvent(event))
		{
			if (event.type == Event::Closed) window.close();
			if (event.type == Event::KeyPressed)
			{
				// Получаем нажатую клавишу - выполняем соответствующее действие
				if (event.key.code == Keyboard::Escape)
				{
					menu(window);
				}
				if (event.key.code == Keyboard::Left)
				{
					game.Move(Direction::Left);
				}
				if (event.key.code == Keyboard::Right)
				{
					game.Move(Direction::Right);
				}
				if (event.key.code == Keyboard::Up)
				{
					game.Move(Direction::Up);
				}
				if (event.key.code == Keyboard::Down)
				{
					game.Move(Direction::Down);
				}
				// Новая игра
				if (event.key.code == Keyboard::F3)
				{
					game.Init();
					move_counter = 100;
				}
			}
		}

		// Если счетчик ходов больше нуля, продолжаем перемешивать головоломку
		if (move_counter-- > 0)
		{
			game.Move((Direction)(rand() % 4));
		}

		// Выполняем необходимые действия по отрисовке
		window.clear();
		window.draw(game);
		window.draw(text);
		window.display();
	}

	return 0;
}